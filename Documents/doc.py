class Info:
    FILE_PATH = './TSchedule_Руководство_По_Стилю.docx'

try:
    from docx import Document

    # Создаем новый документ
    doc = Document()

    # Заголовок документа
    doc.add_heading('Руководство по стилю для приложения TSchedule на WPF', 0)

    # Оглавление
    doc.add_paragraph('Содержание')
    doc.add_paragraph('1. Обзор')
    doc.add_paragraph('2. Цвета')
    doc.add_paragraph('3. Типографика')
    doc.add_paragraph('4. Макет и отступы')
    doc.add_paragraph('5. Стилизация элементов управления')
    doc.add_paragraph('6. Дополнительные рекомендации')
    doc.add_paragraph('')

    # Раздел 1: Обзор
    doc.add_heading('1. Обзор', level=1)
    doc.add_paragraph('TSchedule — это настольное приложение на основе WPF, созданное в соответствии с принципами стандартного UI/UX Windows. '
                      'Этот документ описывает спецификации дизайна внешнего вида приложения, обеспечивая соответствие нативным приложениям Windows.')

    # Раздел 2: Цвета
    doc.add_heading('2. Цвета', level=1)
    doc.add_paragraph('TSchedule использует стандартную цветовую палитру современных настольных приложений Windows. '
                      'Все цвета указаны в формате RGBA.')

    # Цветовая палитра
    doc.add_heading('2.1. Цветовая палитра', level=2)
    table = doc.add_table(rows=1, cols=2)  # Начинаем с одной строки для заголовков
    table.style = 'Table Grid'
    table.cell(0, 0).text = 'Элемент'
    table.cell(0, 1).text = 'Цвет (RGBA)'

    # Таблица цветов
    colors = [
        ('Фон', '#FFFFFFFF'),
        ('Текст', '#FF000000'),
        ('Фон основной кнопки', '#FF0078D7'),
        ('Текст основной кнопки', '#FFFFFFFF'),
        ('Фон второстепенной кнопки', '#FFE1E1E1'),
        ('Текст второстепенной кнопки', '#FF000000')
    ]

    # Добавляем строки в таблицу
    for element, color in colors:
        row_cells = table.add_row().cells  # Добавляем новую строку
        row_cells[0].text = element
        row_cells[1].text = color

    # Раздел 3: Типографика
    doc.add_heading('3. Типографика', level=1)
    doc.add_paragraph('Для обеспечения единообразия с нативными приложениями Windows должны использоваться следующие шрифты и размеры.')

    # Таблица шрифтов
    table = doc.add_table(rows=1, cols=3)  # Начинаем с одной строки для заголовков
    table.style = 'Table Grid'
    table.cell(0, 0).text = 'Элемент текста'
    table.cell(0, 1).text = 'Шрифт'
    table.cell(0, 2).text = 'Размер'

    typography = [
        ('Заголовок окна', 'Segoe UI', '14pt, Bold'),
        ('Заголовок раздела', 'Segoe UI', '12pt, Bold'),
        ('Основной текст', 'Segoe UI', '11pt, Regular'),
        ('Текст кнопок', 'Segoe UI', '11pt, Regular')
    ]

    # Добавляем строки в таблицу
    for element, font, size in typography:
        row_cells = table.add_row().cells  # Добавляем новую строку
        row_cells[0].text = element
        row_cells[1].text = font
        row_cells[2].text = size

    # Раздел 4: Макет и отступы
    doc.add_heading('4. Макет и отступы', level=1)
    doc.add_paragraph('Для поддержания чистого и организованного макета TSchedule необходимо соблюдать следующие правила размещения и отступов:')

    doc.add_heading('4.1. Отступы и внутренние поля', level=2)
    doc.add_paragraph('• Стандартные отступы окна: 10px\n'
                      '• Внутренние поля вокруг кнопок и элементов управления: 8px\n'
                      '• Расстояние между связанными элементами управления: 6px\n'
                      '• Расстояние между несвязанными элементами управления: 12px')

    doc.add_heading('4.2. Выравнивание', level=2)
    doc.add_paragraph('• Все тексты и элементы управления должны быть выровнены по левому краю, если не указано иное.\n'
                      '• Кнопки и элементы ввода должны быть выровнены по горизонтали.\n'
                      '• Метки должны быть выровнены по вертикали с соответствующими элементами управления.')

    # Раздел 5: Стилизация элементов управления
    doc.add_heading('5. Стилизация элементов управления', level=1)

    doc.add_heading('5.1. Кнопки', level=2)
    doc.add_paragraph('Основные кнопки должны использовать основной цвет фона и текста из цветовой палитры. Второстепенные кнопки используют более светлый фон.'
                      ' Углы кнопок должны иметь радиус 4px.')

    doc.add_heading('5.2. Текстовые поля', level=2)
    doc.add_paragraph('Текстовые поля должны использовать стандартный цвет текста и фона из палитры. Они должны иметь границу толщиной 1px с закругленными углами (радиус: 4px).')

    doc.add_heading('5.3. Выпадающие списки и флажки', level=2)
    doc.add_paragraph('Выпадающие списки и флажки должны соответствовать стандартному виду элементов управления Windows, без дополнительной настройки, чтобы сохранить нативный вид.')

    doc.add_heading('5.4. Панели', level=2)
    doc.add_paragraph('Панели должны быть стандартного цвета (оттенки белого и чёрного), иметь внутренние отступы (padding: 8px или 16px), а также иметь закругленные углы (радиус: 8px).')

    # Раздел 6: Дополнительные рекомендации
    doc.add_heading('6. Дополнительные рекомендации', level=1)
    doc.add_paragraph('TSchedule должен быть полностью доступным, поддерживая навигацию с помощью клавиатуры и программы чтения с экрана. Подсказки должны быть предоставлены для всех интерактивных элементов.')

    # Сохраняем документ
    doc.save(Info.FILE_PATH)
    print('Формирование файла выполнено успешно')

except Exception as e:
    print(e)
